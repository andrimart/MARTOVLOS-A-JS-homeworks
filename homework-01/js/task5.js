// 5. Використовуючи функцію confirm() задайте користувачу питання про
// досягнення ним повнолітнього віку. Результат запишіть в змінну isAdult
// і виведіть в консоль.

let isAdult = confirm('Чи Ви досягнули повноліття?');

if (isAdult) {
    document.write('Так, я повнолітній.')
} else {
    document.write('Ні, я ще не повнолітній.')
}

console.log(isAdult);
